        <br><br><h4 style="color:#004E5C;"><strong>18. What can affect broadband speed?</strong></h4>
        <p>Factors that may affect the accuracy of your broadband speed measurement — and your broadband speed — include:</p>
        <ul>
        <li>Downloads &#8211; Make sure you are not downloading anything on your computer. This can adversely affect average speeds during the test</li>
        <li>Wireless routers – Wireless broadband data needs to travel through the air. Thick walls may adversely affect your broadband speed because they’re harder for the signal to pass through. The distance of your computer from the router also affects connection quality.</li>
        <li>Data-heavy programs &#8211; Shut down any software that frequently accesses the internet during the test (e.g. web radio, RSS feeds or TV on-demand services).</li>
        <li>Concurrent users – Turn off other devices using your wireless or fixed line network during the test. Multiple connections to a single router can adversely affect broadband speeds.</li>
        <li>Network coverage – With mobile internet connections, being on the move can affect the quality of your broadband connection. Mobile broadband speeds are more accurately measured on a computer at a static location.</li>
        <li>Broadband network congestion / time of day – Many broadband providers suffer from traffic congestion at the busiest times of day (usually 8pm to 10pm). Speeds suffer during these periods due to the sheer amount of consumers who are online.</li>
        </ul>
        
