        <p>To get the most accurate possible broadband speed test result, follow these simple steps:</p>
        <ul>
        <li>Make sure you are not downloading anything on your PC / laptop.</li>
        </ul>
        <ul>
        <li>Shut down any software that uses the internet (e.g. web radio, RSS news feeds or TV services).</li>
        </ul>
        <ul>
        <li>Turn off any other computers, tablets, smartphones and games consoles that are sharing your wireless network.</li>
        </ul>
        <ul>
        <li>Ensure all broadband cables are properly and securely connected.</li>
        </ul>
        <ul>
        <li>With wireless routers, make sure you are as close to your wireless router as possible. Ideally you’ll be less than 10 metres away.</li>
        </ul>
        <ul>
        <li>Make sure there are no large objects between your computer and wireless router.</li>
        </ul>
        <ul>
        <li>Electronic devices such as microwaves, wireless doorbells, baby monitors, bug zappers and even electric blankets can affect the signal strength, so switch them all off.</li>
        </ul>
