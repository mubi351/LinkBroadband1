			  <br><br><h4 style="color:#004E5C;"><b>19. What is ‘pro rata’ billing?</b></h4>
        <p>Your subscriptions are paid for in advance and invoices are based on a set monthly bill date &#8211; usually 1st of every month. On a connection’s first invoice, or first invoice after a tariff change, there will often be some pro rata billing.</p>
        <p>Example &#8211; First invoice since connection This is when the additional line rental cost that hadn’t been charged in advance is added to the first full month’s line rental cost. For example, if your monthly line rental is £10.00pcm and your subscription is connected on the 15th of the month, you will receive a bill on the 1st of the following month covering all of that month, plus the unpaid 15 days’ line rental from the previous month. Making that bill total £15.00.</p>
        <p>Example &#8211; First invoice after tariff/upgrade amend This is when the system has to balance the total cost of line rental after a mid-month change in monthly invoice value. For example, if the monthly line rental is £10.00pcm and you amend a tariff to £15.00pcm on the 15th of the month, the system initially recognises that it has overcharged you for the £10.00 tariff, so it will credit £5.00 back. It will then invoice the new cost for the remainder of that month (£7.50), plus the following month in advance as standard. The bill on the 1st of the following month, after the amend, will show in the summary a line rental cost of £22.50 with a credit for the £5.00 owed, meaning that month’s total line rental cost was £17.50</p>
        
                


